# GIT - Controle de versões

# Comandos GIT 
 
# git --version| Retorna a versão instalada do GIT  
# git init | Inicia o GIT no diretório escolhido
# git config --global user.name "Beltrano de Carvalho"
# git config --global user.email "beltrano@ctrlplay.com.br"

# git add ./arquivo | Prepara o arquivo para o commit
# git commit -a -m "Mensagem do commit" | Realiza o commit das alterações
# git push | Envia as alterações para o repositório remoto

# git pull | Puxa as alterações do repositório remoto

# git clone url | Cria uma copia ( clone ) do repositório do git hub

# git status | Verifica o status do repositório